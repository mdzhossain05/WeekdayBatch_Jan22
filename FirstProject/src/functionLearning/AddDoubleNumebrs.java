package functionLearning;

public class AddDoubleNumebrs {
	
	public void addTwoNumbers(double numberOne, double numberTwo) {
		double sum = numberOne + numberTwo;
		System.out.println(sum);
	}
	
	public void addThreeNumbers(double numberOne, double numberTwo, double numberThree) {
		double sum = numberOne + numberTwo + numberThree;
		System.out.println(sum);
	}
	
	public void addFourNumbers(double numberOne, double numberTwo, double numberThree, double numberFour) {
		double sum = numberOne + numberTwo + numberThree + numberFour;
		System.out.println(sum);
	}
}
